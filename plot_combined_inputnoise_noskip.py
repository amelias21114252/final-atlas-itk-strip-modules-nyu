import os
import json
import numpy as np
import matplotlib.pyplot as plt
import glob
import argparse
from datetime import datetime
from matplotlib import cm
import csv

def flatten(input):
    if isinstance(input, list) and all(isinstance(x, (int, float)) for x in input):
        return input
    elif isinstance(input, list) and all(isinstance(x, list) for x in input):
        return [item for sublist in input for item in sublist]
    else:
        raise TypeError(f"Expected list of floats or list of lists, got: {type(input)}")

def parse_timestamp(ts_str):
    try:
        return datetime.strptime(ts_str, "%Y-%m-%d %H:%M:%S")
    except Exception:
        return datetime.min

def plot_combined_stream(json_paths, stream, save_dir, serial_number, parent_name=None):
    file_data = []
    warm_noise_values = []
    cold_noise_values = []
    skipped_summary = []

    for path in json_paths:
        with open(path, 'r') as f:
            data = json.load(f)

        try:
            noise_raw = data["results"]["innse_under" if stream == 'under' else "innse_away"]
            noise = flatten(noise_raw)
        except Exception as e:
            print(f"❌ Skipping {os.path.basename(path)} — invalid noise structure ({e})")
            continue

        filename = os.path.basename(path)
        low_vals = [val for val in noise if val < 600]
        high_vals = [val for val in noise if val > 1000]

        if low_vals or high_vals:
            print(f"⚠ Logged from {filename}: low={low_vals} high={high_vals}")
            skipped_summary.append({
                "filename": filename,
                "low_count": len(low_vals),
                "low_values": low_vals,
                "high_count": len(high_vals),
                "high_values": high_vals
            })

        kept = noise  # ✅ Keep all values for plotting

        temp = data["properties"]["DCS"]["AMAC_NTCpb"]
        raw_time = data.get("timestamp", "")
        timestamp = raw_time.replace("T", " ").replace("Z", " ") if "T" in raw_time else raw_time or None

        file_data.append({
            'file': filename,
            'temp': temp,
            'noise': kept,
            'timestamp': timestamp
        })

        if temp > 0:
            warm_noise_values.extend(kept)
        else:
            cold_noise_values.extend(kept)

    cold_data = sorted([d for d in file_data if d['temp'] <= 0], key=lambda x: parse_timestamp(x['timestamp']))
    warm_data = sorted([d for d in file_data if d['temp'] > 0], key=lambda x: parse_timestamp(x['timestamp']))

    cold_cmap = cm.get_cmap('Blues', max(len(cold_data), 1))
    warm_cmap = cm.get_cmap('Oranges', max(len(warm_data), 1))

    cold_mean, cold_std = (np.mean(cold_noise_values), np.std(cold_noise_values)) if cold_noise_values else (np.nan, np.nan)
    warm_mean, warm_std = (np.mean(warm_noise_values), np.std(warm_noise_values)) if warm_noise_values else (np.nan, np.nan)

    plt.figure(figsize=(14, 6))
    legend_entries = []
    first_timestamp = next((d['timestamp'] for d in file_data if d['timestamp']), None)

    for i, entry in enumerate(cold_data):
        color = cold_cmap(i)
        μ = np.mean(entry['noise'])
        σ = np.std(entry['noise'])
        label = f"cold_{i+1:02d} T={entry['temp']:.1f}°C | μ={μ:.1f}, σ={σ:.1f}"
        plt.hist(entry['noise'], bins=40, alpha=0.5, color=color, edgecolor='black', linewidth=0.3)
        plt.axvline(μ, color=color, linestyle='dashed', linewidth=1)
        legend_entries.append(label)

    for i, entry in enumerate(warm_data):
        color = warm_cmap(i)
        μ = np.mean(entry['noise'])
        σ = np.std(entry['noise'])
        label = f"warm_{i+1:02d} T={entry['temp']:.1f}°C | μ={μ:.1f}, σ={σ:.1f}"
        plt.hist(entry['noise'], bins=40, alpha=0.5, color=color, edgecolor='black', linewidth=0.3)
        plt.axvline(μ, color=color, linestyle='dashed', linewidth=1)
        legend_entries.append(label)

    if not np.isnan(cold_mean):
        plt.axvline(cold_mean, color='blue', linestyle='dashed', linewidth=2.5)
        legend_entries.append(f"All Cold μ={cold_mean:.1f}, σ={cold_std:.1f}")
    if not np.isnan(warm_mean):
        plt.axvline(warm_mean, color='orange', linestyle='dashed', linewidth=2.5)
        legend_entries.append(f"All Warm μ={warm_mean:.1f}, σ={warm_std:.1f}")

    plt.xlabel('Input Noise [ENC]')
    plt.ylabel('Counts')
    plt.ylim(0, 150)
    plt.xlim(700, 1100)

    title_str = f"Module SN: SN{serial_number} | Parent: SN{parent_name}\nOverlaid {stream} Histograms"
    if first_timestamp:
        title_str += f"\nTimestamp: {first_timestamp}"
    plt.title(title_str)

    skipped_total = sum(d["low_count"] + d["high_count"] for d in skipped_summary)
    if skipped_total > 0:
        plt.text(0.98, 0.95, f"Out-of-range values: {skipped_total} (<600 or >1000)",
                 transform=plt.gca().transAxes,
                 color='red', fontsize='small',
                 ha='right', va='top')

    plt.grid(True)
    plt.legend(legend_entries, fontsize='x-small', loc='center left', bbox_to_anchor=(-0.02, 0.5))
    plt.tight_layout()

    os.makedirs(save_dir, exist_ok=True)
    base = os.path.join(save_dir, f"SN{serial_number}_combined-{stream}")
    plt.savefig(f"{base}.pdf")
    plt.savefig(f"{base}.png", dpi=300)
    print(f"✅ Saved: {base}.pdf and .png")

    # Save JSON/CSV of low/high values
    if skipped_summary:
        csv_path = os.path.join(save_dir, f"SN{serial_number}_{stream}_low_high_values.csv")
        with open(csv_path, mode='w', newline='') as csvfile:
            writer = csv.writer(csvfile)
            writer.writerow(['filename', 'low_count', 'low_values', 'high_count', 'high_values'])
            for entry in skipped_summary:
                writer.writerow([
                    entry['filename'],
                    entry['low_count'],
                    json.dumps(entry['low_values']),
                    entry['high_count'],
                    json.dumps(entry['high_values'])
                ])
        print(f"📝 CSV of low/high values saved to: {csv_path}")

        json_path = os.path.join(save_dir, f"SN{serial_number}_{stream}_skipped.json")
        with open(json_path, 'w') as jf:
            json.dump(skipped_summary, jf, indent=2)
        print(f"📝 JSON of low/high values saved to: {json_path}")

def main():
    parser = argparse.ArgumentParser(description="Plot combined input noise histograms")
    parser.add_argument("--serial_number", required=True, help="e.g. 20USBHX2002592")
    args = parser.parse_args()

    serial = args.serial_number
    input_dir = f"SN{serial}"
    file_pattern = f"SN{serial}_*.json"
    file_paths = sorted(glob.glob(os.path.join(input_dir, file_pattern)))
    output_dir = os.path.join(input_dir, "histograms_combined_noskip")

    if not file_paths:
        print("⚠️ No matching JSON files found.")
        return

    parent_name = None
    try:
        with open(file_paths[0], 'r') as f:
            data = json.load(f)
            parent_name = data.get("parent_name", None)
    except Exception as e:
        print(f"⚠️ Could not retrieve parent name: {e}")

    plot_combined_stream(file_paths, 'under', output_dir, serial, parent_name)
    plot_combined_stream(file_paths, 'away', output_dir, serial, parent_name)

if __name__ == "__main__":
    main()
