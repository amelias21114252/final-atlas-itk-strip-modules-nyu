import argparse
import os
import json
from pprint import pprint
import itkdb

def get_test_run(serial_number="20USBHX2002657", test_name='Response Curve TC'):
    '''
    Queries ITk Production DB for a given test and splits data into 25 JSON files
    '''
    # Setup ITk client
    token = os.getenv("ITK_DB_AUTH")
    user = itkdb.core.UserBearer(bearer=token)
    client = itkdb.Client(user=user)

    # Get test run
    component = client.get("getComponent", json={"component": serial_number})
    testID = [
        y["id"]
        for x in component["tests"]
        for y in x["testRuns"]
        if x["name"] == test_name
    ]
    print(f"Serial number: {serial_number}")
    print(f"Test name: {test_name}")
    print(f"Getting test ID: {testID}")
    testRun = client.get("getTestRun", json={"testRun": testID[0]})

    # Extract data
    props = testRun['properties'][0]['value']
    amac_ntcpb = props.get('AMAC_NTCpb')
    amac_ntcx = props.get('AMAC_NTCx')
    amac_ntcy = props.get('AMAC_NTCy')

    innse_under = next((x['value'] for x in testRun['results'] if x['name'] == 'innse_under'), None)
    innse_away = next((x['value'] for x in testRun['results'] if x['name'] == 'innse_away'), None)

    timestamp = testRun['components'][0].get('stateTs')
    component_info = testRun.get('components', {})

    # Get parent name (module serial number) if hybrid
    parent_name = None
    if 'HX' in serial_number or 'HY' in serial_number:
        try:
            parent_name = testRun['components'][0]['ancestorMap']['parent']['component']['serialNumber']
            print(f"Module (parent) serial number: {parent_name}")
        except KeyError:
            print("⚠️ Could not retrieve parent module serial number.")

    # Split into 25 JSON files
    output_dir = "SN" + serial_number
    os.makedirs(output_dir, exist_ok=True)

    for i in range(25):
        split_data = {
            "serial_number": serial_number,
            "test_name": test_name,
            "test_id": testID[0],
            "timestamp": timestamp,
            "parent_name": parent_name,
            "properties": {
                "DCS": {
                    "AMAC_NTCpb": amac_ntcpb[i],
                    "AMAC_NTCx": amac_ntcx[i],
                    "AMAC_NTCy": amac_ntcy[i]
                },
                "det_info": {
                    "Address": list(range(10)),
                    "Channel": list(range(1, 11)),
                    "name": f"SN{serial_number}"
                },
                "fit_type_code": 4
            },
            "results": {
                "innse_under": innse_under[i],
                "innse_away": innse_away[i]
            },
            "component_info": component_info
        }

        filename = os.path.join(output_dir, f"SN{serial_number}_{i+1:02}.json")
        with open(filename, "w") as f_out:
            json.dump(split_data, f_out, indent=2)

    print(f"\n✅ Split into 25 files complete. Saved to folder: {output_dir}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Query ITk DB and export split JSONs")
    parser.add_argument("--serial_number", required=True, help="Serial number e.g. 20USBHX2002657")
    parser.add_argument("--test_name", default="Response Curve TC", help="Test name")
    args = parser.parse_args()

    get_test_run(args.serial_number, args.test_name)
