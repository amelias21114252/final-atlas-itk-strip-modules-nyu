import argparse
import os
import json
from pprint import pprint
import itkdb

def get_test_run(serial_number="20USBHX2002592", test_name='Response Curve TC'):
    token = os.getenv("ITK_DB_AUTH")
    user = itkdb.core.UserBearer(bearer=token)
    client = itkdb.Client(user=user)

    component = client.get("getComponent", json={"component": serial_number})
    testID = [
        y["id"]
        for x in component["tests"]
        for y in x["testRuns"]
        if x["name"] == test_name
    ]

    print(f"Serial number: {serial_number}")
    print(f"Test name: {test_name}")
    print(f"Getting test ID: {testID}")

    testRun = client.get("getTestRun", json={"testRun": testID[0]})
    timestamp = testRun["components"][0]["stateTs"]

    # Extract results
    innse_under, innse_away, current, voltage = [], [], [], []

    for x in testRun["results"]:
        if x["name"] == "innse_under":
            innse_under = x["value"]
        elif x["name"] == "innse_away":
            innse_away = x["value"]
        elif x["code"] == "CURRENT":
            current = x["value"]
        elif x["code"] == "VOLTAGE":
            voltage = x["value"]

    props = testRun["properties"][0]["value"]
    amac_pb = props.get("AMAC_NTCpb")
    amac_x = props.get("AMAC_NTCx")
    amac_y = props.get("AMAC_NTCy")

    # Create output directory using SN prefix
    output_dir = f"SN{serial_number}"
    os.makedirs(output_dir, exist_ok=True)

    # Write 25 JSON files
    for i in range(24):
        json_data = {
            "serial_number": serial_number,
            "test_name": test_name,
            "test_id": testID[0],
            "timestamp": timestamp,
             "properties": {
                "DCS": {
                    "AMAC_NTCpb": amac_pb[i] if amac_pb and i < len(amac_pb) else None,
                    "AMAC_NTCx":  amac_x[i]  if amac_x  and i < len(amac_x)  else None,
                    "AMAC_NTCy":  amac_y[i]  if amac_y  and i < len(amac_y)  else None
                },
                "det_info": {
                    "Address": list(range(10)),
                    "Channel": list(range(1, 11)),
                    "name": f"SN{serial_number}"
                },
                "fit_type_code": 4
            },
            "index": i,
            "results": {
                "innse_under": innse_under[i] if i < len(innse_under) else None,
                "innse_away": innse_away[i] if i < len(innse_away) else None,
                "current": current[i] if i < len(current) else None,
                "voltage": voltage[i] if i < len(voltage) else None
            },
        }

        filename = os.path.join(output_dir, f"SN{serial_number}_{i+1:02}.json")
        with open(filename, "w") as f:
            json.dump(json_data, f, indent=2)

        print(f"✅ Wrote: {filename}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Split and save ITk test results into 25 JSON files")
    parser.add_argument("--serial_number", help="Serial number e.g. 20USBHX2002592")
    parser.add_argument("--test_name", help="Name of test e.g. 'Module AMAC IV TC'")

    args = parser.parse_args()
    serial_number = args.serial_number or "20USBHX2002592"
    test_name = args.test_name or "Response Curve TC"

    get_test_run(serial_number, test_name)
